-- Run: supabase db push  أو الصق في SQL Editor
-- ---------------------------------------------------------------------
-- شيل حد الحجم الافتراضي (file_size_limit) عن bucket "eduverse-media"
-- (اللي أنشأته 0004_media_storage.sql) عشان الملفات الكبيرة (فيديوهات
-- طويلة، ملفات ضخمة...) ترفع من غير رفض تلقائي بسبب حجمها من
-- Supabase Storage.
--
-- ⚠️ تنبيه مهم: الشيل ده بيلغي القيد على مستوى الـ bucket نفسه بس.
-- مشروع Supabase عندك (حسب خطتك Free/Pro/...) عنده سقف أقصى فعلي لحجم
-- الملف الواحد على مستوى المشروع كله بيُضبط من:
--   Dashboard → Project Settings → Storage → "Upload file size limit"
-- لو محتاج ترفع ملفات أكبر من السقف ده، لازم تزوّده من هناك كمان —
-- ده حد بتاع Supabase نفسه ومش حاجة SQL وحدها تقدر تتحكم فيها بالكامل.
-- =====================================================================

update storage.buckets
  set file_size_limit = null
  where id = 'eduverse-media';
