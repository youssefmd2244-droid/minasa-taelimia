/**
 * genId — بديل آمن لـ `Date.now()` كمعرّف فريد.
 * -----------------------------------------------------------------------
 * المشكلة: `Date.now()` بيرجّع رقم المللي ثانية الحالي بس، فلو المستخدم
 * ضاف أكتر من عنصر (قسم / محتوى / ملف / تسجيل) بسرعة (زرار "إضافة"
 * ضغطتين قريبين من بعض، أو أي كود بينفذ أكتر من إضافة في نفس اللحظة)،
 * ممكن يرجع نفس الرقم بالظبط لعنصرين مختلفين. لما ده يحصل:
 *   - React بيستخدم الـ id ده كـ `key` — مفتاحين متطابقين يخلي React
 *     يلخبط في عرض العنصرين، فبيظهر واحد بس بدل الاتنين.
 *   - كل عمليات التحديث اللي بتدور بـ `.id === item.id` (تمييز/حذف/
 *     تفعيل "رئيسية"...) بتأثر على العنصرين مع بعض لأن معرفهم واحد.
 * الحل: نضيف عداد صغير مع الوقت عشان نضمن رقم مختلف حتى لو نفس المللي
 * ثانية بالظبط.
 */
let counter = 0;

export function genId(): number {
  counter = (counter + 1) % 1000;
  return Date.now() * 1000 + counter;
}
